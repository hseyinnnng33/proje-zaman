function randomOlay(){
    if (dataCopy.length === 0) {
        dataCopy = [...data];
      }

    const random = Math.floor(Math.random() * dataCopy.length)
    const randomData = dataCopy.splice(random, 1)[0];

    kart.innerHTML = `
    <div class="sol">
        <h2 class="tarih">${randomData.zaman}</h2>
            <img src="${randomData.photo}" class="resim">
    </div>
    <div class="kartlar">    
        <div class="onemliKonu">
            <h1>En Önemli Olay</h1>
            <h2 class="onemliOlay">${randomData.olay}</h2>
            <p class="text">${randomData.text}</p>
            </div>
        </div>
        <div class="kisiler">
        <h2>Sözler ve Olaylar</h2>
        <span>- ${randomData.sozler1}</span>
        <span>- ${randomData.sozler2}</span>
        <span>- ${randomData.sozler3}</span>
        <span>- ${randomData.sozler4}</span>
        <span>- ${randomData.sozler5}</span>
        </div>
    `
}
